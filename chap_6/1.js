// 1. Create a function that adds two numbers
function addNumbers(a, b) {
    return a + b;
  }
  
  // 2. Set up two different variables
  let num1 = 5;
  let num2 = 10;
  
  // 3. Use the function on the two variables and log the result
  console.log("Result 1:", addNumbers(num1, num2)); // Output: 15
  
  // 4. Second call to the function with different numbers
  console.log("Result 2:", addNumbers(20, 30)); // Output: 50
  