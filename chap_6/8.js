// Function expression assigned to a variable
const showMessage = function(message) {
    console.log("Function Expression:", message);
  };
  
  // 2. Pass an argument into the function
  showMessage("Hello from a function expression!");
  
  // Function declaration
function showMessageDecl(message) {
    console.log("Function Declaration:", message);
  }
  
  // Call the function with an argument
  showMessageDecl("Hello from a function declaration!");
  